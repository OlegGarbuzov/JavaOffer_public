package com.example.javaoffer.exam.enums;

import lombok.Getter;

/**
 * Перечисление, представляющее режимы экзаменов в системе.
 * <p>
 * Определяет способ проведения экзамена и связанные с ним возможности:
 * соревновательный рейтинговый режим с проверкой на античит или
 * свободный режим для тренировки без рейтинга.
 *
 * @author Garbuzov Oleg
 */
@Getter
public enum ExamMode {
	/**
	 * Свободный (тренировочный) режим.
	 * <p>
	 * Используется для тренировки и ознакомления с вопросами.
	 * Не влияет на рейтинг пользователя, не проверяется системой античит.
	 */
	FREE,

	/**
	 * Рейтинговый (соревновательный) режим.
	 * <p>
	 * Результаты влияют на рейтинг пользователя, активирована система античит,
	 * которая отслеживает нарушения во время прохождения экзамена.
	 */
	RATING
}
