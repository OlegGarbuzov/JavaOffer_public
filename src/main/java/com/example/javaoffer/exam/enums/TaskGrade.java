package com.example.javaoffer.exam.enums;

/**
 * Перечисление, представляющее уровни квалификации, для которых предназначены задания.
 * <p>
 * Соответствует общепринятым уровням разработчиков в IT-индустрии.
 * Используется для классификации вопросов по целевой аудитории и 
 * формирования экзаменов соответствующего уровня сложности.
 * 
 *
 * @author Garbuzov Oleg
 */
public enum TaskGrade {
    /**
     * Начальный уровень квалификации (младший разработчик).
     * <p>
     * Вопросы для разработчиков с небольшим опытом работы (обычно до 1-2 лет).
     * Охватывает базовые знания языка и технологий.
     * 
     */
    JUNIOR,
    
    /**
     * Средний уровень квалификации (разработчик среднего уровня).
     * <p>
     * Вопросы для разработчиков со значительным опытом работы (обычно 2-5 лет).
     * Требуют углубленного понимания языка, технологий и архитектурных решений.
     * 
     */
    MIDDLE,
    
    /**
     * Высокий уровень квалификации (старший разработчик).
     * <p>
     * Вопросы для опытных разработчиков (обычно от 5 лет опыта).
     * Охватывают продвинутые концепции, оптимизацию, архитектуру и глубокое
     * понимание внутренних механизмов технологий.
     * 
     */
    SENIOR
}
