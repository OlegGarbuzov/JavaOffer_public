package com.example.javaoffer.exam.validation;

import com.example.javaoffer.exam.enums.ExamMode;

/**
 * Интерфейс для валидации процесса старта экзамена.
 * <p>
 * Определяет контракт для различных видов валидации, которые должны быть выполнены
 * перед началом экзамена. Реализации этого интерфейса могут проверять аутентификацию
 * пользователя, наличие прав доступа, соблюдение правил режима экзамена и другие
 * бизнес-условия.
 * 
 *
 * @author Garbuzov Oleg
 */
public interface ExamStartProcessValidation {
	
	/**
	 * Выполняет валидацию процесса старта экзамена для указанного режима.
	 * <p>
	 * Метод должен проверить все необходимые условия для начала экзамена
	 * в указанном режиме и бросить соответствующее исключение, если
	 * валидация не прошла.
	 * 
	 * 
	 * @param examMode режим экзамена, для которого выполняется валидация
	 * @throws RuntimeException если валидация не прошла
	 */
	void validation(ExamMode examMode);
}
